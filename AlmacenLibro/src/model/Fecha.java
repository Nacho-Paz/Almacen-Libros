package model;

/**
 *
 * @author ignac
 */
public class Fecha {
    private int idFecha;
    private int dia;
    private int mes;
    private int anio;
    
    public Fecha(){}

    public Fecha(int dia, int mes, int anio) {
        this.dia = dia;
        this.mes = mes;
        this.anio = anio;
    }

    public Fecha(int idFecha, int dia, int mes, int anio) {
        this.idFecha = idFecha;
        this.dia = dia;
        this.mes = mes;
        this.anio = anio;
    }

    public int getIdFecha() {
        return idFecha;
    }

    public void setIdFecha(int idFecha) {
        this.idFecha = idFecha;
    }

    public int getDia() {
        return dia;
    }

    public void setDia(int dia) {
        this.dia = dia;
    }

    public int getMes() {
        return mes;
    }

    public void setMes(int mes) {
        this.mes = mes;
    }

    public int getAnio() {
        return anio;
    }

    public void setAnio(int anio) {
        this.anio = anio;
    }

    @Override
    public String toString() {
        return dia + " / " + mes + " / " + anio;
    }
    
    
}
